# 1. Combining two strings:
# Create two separate "boxes"
first_name = "Jane"
last_name = "Doe"

# We combine them into a third "box"
# We add in the " " in the middle so there is a space between the names!
full_name = first_name + " " + last_name

print(full_name) # This will now show: Jane Doe

# 2. Updating a Variable 
# Start with 0
my_score = 0

# Now, tell the Variable to be "whatever it was before, plus 10"
my_score = my_score + 10

print(my_score) # This will now show: 10

# 3. Changing a number into Text

age = 12

# This tells Python: Turn the number 12 into the word '12'"
age_as_text = str(age)

print("I am " + age_as_text + "years old.")

